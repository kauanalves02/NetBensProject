    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String materiais = campoMateriais.getText();
        String valores = campoValores.getText();
        
        Produto c =  new Produto();
        c.setMateriais(materiais);
        c.setValores(valores);
        
        new ProdutoDAO().inserir(c);
        
        JOptionPane.showMessageDialog(null,"Produto renovado");
        campoMateriais.setText("");
        campoValores.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

