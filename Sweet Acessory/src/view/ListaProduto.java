    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
         int linha = tbProduto.getSelectedRow();
         int id = (int)tbProduto.getValueAt(linha,0 );
         String materiais = (String)tbProduto.getValueAt(linha, 1);
         String valores = (String)tbProduto.getValueAt(linha, 2);
         
         Produto c = new Produto();
         c.setId(id);
         c.setMateriais(materiais);
         c.setValores(valores);
         AtualizaProduto ac = new AtualizaProduto();
         ac.setVisible(true);
         ac.setForm(c);
         
    }//GEN-LAST:event_btnAtualizarActionPerformed

